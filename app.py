import torch
import torch.nn as nn
import torch.nn.functional as F
import json
import gradio as gr


device = 'cuda' if torch.cuda.is_available() else 'cpu'


VOCAB_PATH = './vocab'
MODEL_PATH = './trained_model'

with open(f"{VOCAB_PATH}/vocab.json", "r") as vocab_file:
    vocab = json.load(vocab_file)['chars']

with open(f"{VOCAB_PATH}/encode.json", "r") as encode_file:
    stoi = json.load(encode_file)

with open(f"{VOCAB_PATH}/decode.json", "r") as decode_file:
    itos = json.load(decode_file)


encode = lambda s: [stoi[c] for c in s]
decode = lambda l: ''.join([itos[str(i)] for i in l])

vocab_size = len(vocab)
n_embd = 384
block_size = 256
n_head = 6
n_layer = 6
dropout = 0.2


class Head(nn.Module):
    def __init__(self, head_size):
        super().__init__()
        self.key = nn.Linear(n_embd, head_size, bias=False)
        self.query = nn.Linear(n_embd, head_size, bias=False)
        self.value = nn.Linear(n_embd, head_size, bias=False)
        self.register_buffer('tril', torch.tril(torch.ones(block_size, block_size)))
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        B, T, C = x.shape
        k = self.key(x)
        q = self.query(x)
        wei = q @ k.transpose(-2, -1) * (C ** -0.5)
        wei = wei.masked_fill(self.tril[:T, :T] == 0, float('-inf'))
        wei = F.softmax(wei, dim=-1)
        wei = self.dropout(wei)
        v = self.value(x)
        return wei @ v

class MultiHeadAttention(nn.Module):
    def __init__(self, num_heads, head_size):
        super().__init__()
        self.heads = nn.ModuleList([Head(head_size) for _ in range(num_heads)])
        self.proj = nn.Linear(num_heads * head_size, n_embd)
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        out = torch.cat([h(x) for h in self.heads], dim=-1)
        out = self.dropout(self.proj(out))
        return out

class FeedFoward(nn.Module):
    def __init__(self, n_embd):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(n_embd, 4 * n_embd),
            nn.ReLU(),
            nn.Linear(4 * n_embd, n_embd),
            nn.Dropout(dropout),
        )

    def forward(self, x):
        return self.net(x)

class Block(nn.Module):
    def __init__(self, n_embd, n_head):
        super().__init__()
        head_size = n_embd // n_head
        self.sa = MultiHeadAttention(n_head, head_size)
        self.ffwd = FeedFoward(n_embd)
        self.ln1 = nn.LayerNorm(n_embd)
        self.ln2 = nn.LayerNorm(n_embd)

    def forward(self, x):
        x = x + self.sa(self.ln1(x))
        x = x + self.ffwd(self.ln2(x))
        return x

class GPTLanguageModel(nn.Module):
    def __init__(self, vocab_size):
        super().__init__()
        self.token_embedding_table = nn.Embedding(vocab_size, n_embd)
        self.position_embedding_table = nn.Embedding(block_size, n_embd)
        self.blocks = nn.Sequential(*[Block(n_embd, n_head) for _ in range(n_layer)])
        self.ln_f = nn.LayerNorm(n_embd)
        self.lm_head = nn.Linear(n_embd, vocab_size)

    def forward(self, idx, targets=None):
        B, T = idx.shape
        tok_emb = self.token_embedding_table(idx)
        pos_emb = self.position_embedding_table(torch.arange(T, device=device))
        x = tok_emb + pos_emb
        x = self.blocks(x)
        x = self.ln_f(x)
        logits = self.lm_head(x)
        return logits

    def generate(self, idx, max_new_tokens):
        for _ in range(max_new_tokens):
            idx_cond = idx[:, -block_size:]
            logits = self(idx_cond)
            logits = logits[:, -1, :]
            probs = F.softmax(logits, dim=-1)
            idx_next = torch.multinomial(probs, num_samples=1)
            idx = torch.cat((idx, idx_next), dim=1)
        return idx


model = GPTLanguageModel(vocab_size)
model.load_state_dict(torch.load(f'{MODEL_PATH}/gpt_epoch_2.pth', map_location=device))
model.to(device)
model.eval()

# Function to generate a response based on input text
def generate_response(prompt):
    context = torch.tensor(encode(prompt), dtype=torch.long).unsqueeze(0).to(device)
    response_idx = model.generate(context, max_new_tokens=100)[0].tolist()
    response = decode(response_idx)
    return response

# Gradio interface
def gradio_interface(prompt):
    return generate_response(prompt)

iface = gr.Interface(fn=gradio_interface, inputs="text", outputs="text", title="Julius Caesar LLM")

# Launch the interface
iface.launch()
